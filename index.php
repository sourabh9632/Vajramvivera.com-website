$("form").each(function () {
  var form = $(this);
  if (form.length) {
      form.validate({
          rules: {
              phone: {
                  required: true,
                  number: true,
              },
              email: {
                  required: true,
                  email: true,
              },
          },
          submitHandler: function (form) {
              var btn = $("#" + form.id + ' [type="submit"]'),
                  _form = $(form),
                  loading = _form.find(".loading");

              loading.fadeIn();
              btn.attr("disabled", true);
              _form.addClass("disabled");

              $.ajax({
                  url: form.action,
                  type: form.method,
                  data: $(form).serialize(),
                  success: function (data) {
                      loading.fadeOut();
                      btn.removeAttr("disabled");
                      _form.removeClass("disabled");

                      if (data) {
                           try {
                               data = jQuery.parseJSON(data);
                           } catch (e) {
                               console.log(e); // error in the above string (in this case, yes)!
                           }
                       }
                       response = data.response;

                      if (response.code === 0) {
                          ohSnap("Failed sending your information, please try again!", { color: "red" });
                      } else if (response.code === 1) {
                          form.reset();
                          ohSnap("Your information successfully reached us.", { color: "green", duration: "1000" });
                          setTimeout(function () {
                              window.location = "https://vajramvivera.com/campagins/thankyou.php";
                          }, 1000);
                      } else if (response.code === 2) {
                          ohSnap("User already exists!", { color: "red" });
                      } else if (response.code === 3) {
                          ohSnap("CAPTCHA Error", { color: "red" });
                      } else {
                          ohSnap("Technical Error: Please contact administrator!", { color: "red" });
                      }
                  },
                  error: function (xhr, status, error) {
                      loading.fadeOut();
                      btn.removeAttr("disabled");
                      _form.removeClass("disabled");
                      ohSnap("Technical Error: Please contact administrator!", { color: "red" });
                  }
              });
              return false;
          }
      });
  }
});
